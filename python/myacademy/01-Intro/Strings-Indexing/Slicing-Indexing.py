# Indexing / Slicing

# Indexing: use []
# Slicing: Grab a subsection of multiple characters [Start:Stop:Step]
   # Step: Size of the jump
   # Stop: Index you will go up to but not included
   # Start: Numerical index for the slice start

# String indexing
str = "My world"
print(str[3:])
print(str[::-1])
print(str[-1])
print(str[0:2])
print(str[1:4])
print(str[:3])


strs="abcdefghijk"
print(strs[3:9])